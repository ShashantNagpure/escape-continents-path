
public class Pair {
	
	public City city;
	public int distance;
	
	public Pair(City city, int distance) {
		super();
		this.city = city;
		this.distance = distance;
	}
	public City getCity() {
		return city;
	}
	public void setCity(City city) {
		this.city = city;
	}
	public int getDistance() {
		return distance;
	}
	public void setDistance(int distance) {
		this.distance = distance;
	}
	
}
